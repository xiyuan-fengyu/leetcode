 */

drop table if exists Customers;
create table Customers (Id int primary key , Name varchar(255));
insert into Customers (Id, Name) values ('1', 'Joe');
insert into Customers (Id, Name) values ('2', 'Henry');
insert into Customers (Id, Name) values ('3', 'Sam');
insert into Customers (Id, Name) values ('4', 'Max');

drop table if exists Orders;
create table Orders (Id int primary key , CustomerId int);
insert into Orders (Id, CustomerId) values ('1', '3');
insert into Orders (Id, CustomerId) values ('2', '1');

select Name Customers from Customers tc left join Orders tor on tc.Id = tor.CustomerId where tor.CustomerId is null;

select Name Customers from Customers where Id not in (select CustomerId from Orders);
